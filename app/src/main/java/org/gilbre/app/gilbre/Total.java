public class Total {

    public String type;
    public String total;

    public Total(String type, String total) {
        this.type = type;
        this.type = total;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
}
